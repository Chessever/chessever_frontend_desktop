  /// Get all folders the current user is subscribed to.
  Future<List<LibraryFolder>> getSubscribedBooks() => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    // Fetch subscription folder IDs
    final subs = await supabase
        .from('book_subscriptions')
        .select('folder_id')
        .eq('subscriber_id', userId);

    final folderIds =
        (subs as List).map((s) => s['folder_id'] as String).toList();
    if (folderIds.isEmpty) return [];

    // Fetch the actual folders (RLS grants access via subscription)
    final folders = await supabase
        .from('user_folders')
        .select()
        .inFilter('id', folderIds)
        .order('name', ascending: true);

    return (folders as List)
        .map(
          (json) =>
              LibraryFolder.fromSupabase(json).copyWith(isSubscribed: true),
        )
        .toList();
  });

  /// Check if the current user is subscribed to a folder.
  Future<bool> isSubscribedToBook(String folderId) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) return false;

    final response =
        await supabase
            .from('book_subscriptions')
            .select()
            .eq('folder_id', folderId)
            .eq('subscriber_id', userId)
            .maybeSingle();

    return response != null;
  });

  /// Stream analyses in a subscribed (shared) folder.
  /// Uses a polling-based approach since Supabase realtime streams
  /// filter by the authenticated user's rows (won't work for shared folders).
  Stream<List<SavedAnalysis>> subscribeSharedFolderAnalyses(
    String folderId,
  ) async* {
    // Fetch immediately, then poll every 10 seconds.
    while (true) {
      final response = await supabase
          .from('user_saved_analyses')
          .select()
          .eq('folder_id', folderId)
          .order('created_at', ascending: false);

      yield (response as List)
          .map((json) => SavedAnalysis.fromSupabase(json))
          .toList();

      await Future.delayed(const Duration(seconds: 10));
    }
  }

