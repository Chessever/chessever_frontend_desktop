  // ============ FOLDER METHODS ============

  /// Get all folders for the current user, ordered by order_index
  Future<List<LibraryFolder>> getFolders() => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    final response = await supabase
        .from('user_folders')
        .select()
        .eq('user_id', userId)
        .order('order_index', ascending: true);

    return (response as List)
        .map((json) => LibraryFolder.fromSupabase(json))
        .toList();
  });

  /// Get a single folder by ID
  Future<LibraryFolder?> getFolder(String folderId) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    final response =
        await supabase
            .from('user_folders')
            .select()
            .eq('id', folderId)
            .eq('user_id', userId)
            .maybeSingle();

    return response != null ? LibraryFolder.fromSupabase(response) : null;
  });

  /// Create a new folder
  Future<LibraryFolder> createFolder({
    required String name,
    String? color,
    String? icon,
    int? orderIndex,
    String? parentId,
  }) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    // If no order index provided, get the next available one
    final nextOrder = orderIndex ?? await _getNextFolderOrder();

    final folder = LibraryFolder(
      id: '', // Will be generated by database
      userId: userId,
      name: name,
      color: color ?? '#0FB4E5',
      icon: icon ?? 'folder',
      orderIndex: nextOrder,
      parentId: parentId,
      createdAt: DateTime.now(),
      updatedAt: DateTime.now(),
    );

    final response =
        await supabase
            .from('user_folders')
            .insert(folder.toSupabaseInsert())
            .select()
            .single();

    return LibraryFolder.fromSupabase(response);
  });

  /// Ensure the user has the default "My Database" structure.
  /// Creates a root "My Database" folder and a child "My Subdatabase" folder.
  Future<void> ensureDefaultFolders() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) return;

    // Check if user already has any folders
    final existing = await supabase
        .from('user_folders')
        .select('id')
        .eq('user_id', userId)
        .limit(1);

    if ((existing as List).isNotEmpty) return;

    // Create root folder
    final rootFolder = await createFolder(name: 'My Database');
    // Create child folder inside it
    await createFolder(name: 'My Subdatabase', parentId: rootFolder.id);
  }

  /// Update a folder
  Future<LibraryFolder> updateFolder(LibraryFolder folder) =>
      handleApiCall(() async {
        final userId = supabase.auth.currentUser?.id;
        if (userId == null) throw Exception('User not authenticated');

        final response =
            await supabase
                .from('user_folders')
                .update({
                  'name': folder.name,
                  'color': folder.color,
                  'icon': folder.icon,
                  'order_index': folder.orderIndex,
                })
                .eq('id', folder.id)
                .eq('user_id', userId)
                .select()
                .single();

        return LibraryFolder.fromSupabase(response);
      });

  /// Delete a folder. FK is ON DELETE CASCADE — every analysis inside is
  /// hard-deleted with the folder. Confirm with the user before calling.
  Future<void> deleteFolder(String folderId) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    await supabase
        .from('user_folders')
        .delete()
        .eq('id', folderId)
        .eq('user_id', userId);
  });

  /// Get the next available order index for folders
  Future<int> _getNextFolderOrder() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) return 0;

    final response =
        await supabase
            .from('user_folders')
            .select('order_index')
            .eq('user_id', userId)
            .order('order_index', ascending: false)
            .limit(1)
            .maybeSingle();

    if (response == null) return 0;
    return (response['order_index'] as int) + 1;
  }

  // ============ SHARING METHODS ============

  static final _random = Random.secure();
  static const _tokenChars =
      'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';

  static String _generateToken([int length = 10]) {
    return List.generate(
      length,
      (_) => _tokenChars[_random.nextInt(_tokenChars.length)],
    ).join();
  }

  /// Generate a share token for a folder and store the owner's display name.
  /// Returns the updated folder with the new share_token.
  Future<LibraryFolder> generateShareToken(String folderId) => handleApiCall(
    () async {
      final user = supabase.auth.currentUser;
      if (user == null) throw Exception('User not authenticated');

      final displayName =
          user.userMetadata?['full_name'] as String? ??
          user.userMetadata?['name'] as String? ??
          user.email?.split('@').first ??
          'Unknown';

      final token = _generateToken();

      final response =
          await supabase
              .from('user_folders')
              .update({'share_token': token, 'owner_display_name': displayName})
              .eq('id', folderId)
              .eq('user_id', user.id)
              .select()
              .single();

      return LibraryFolder.fromSupabase(response);
    },
  );

  /// Revoke sharing for a folder by clearing its share_token.
  Future<void> revokeShareToken(String folderId) => handleApiCall(() async {
    final userId = supabase.auth.currentUser?.id;
    if (userId == null) throw Exception('User not authenticated');

    await supabase
        .from('user_folders')
        .update({'share_token': null})
        .eq('id', folderId)
        .eq('user_id', userId);
  });

